# Arithmetic Operator
a = 10
b = 3

print(a / b)   # 3.333...
print(a // b)  # 3
print(a % b)   # 1
print(a ** b)  # 1000

# Comparison Operator
x = 5
print(x == 5)   # True
print(x>=0)

# Identity Operator
a = [1,2]
b = [1,2]

print(a == b)  # True
print(a is b)  # False

# Logical Operators
x = 5
print(x > 0 and x < 10)  # True

# Membership Operators
numbers = [1,2,3]

print(2 in numbers)      # True
print(5 not in numbers)  # True